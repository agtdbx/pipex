#include "libft.h"

void	ft_bzero(void *s, int n)
{
	int				i;
	unsigned char	*tmp;

	tmp = (unsigned char *) s;
	i = 0;
	while (i < n)
	{
		tmp[i] = '\0';
		i++;
	}
}
