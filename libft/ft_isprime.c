#include "libft.h"

int	ft_isprime(int nb)
{
	int	rep;

	if (nb < 2)
		return (0);
	rep = 2;
	while (rep <= nb / 2)
	{
		if (nb % rep == 0)
			return (0);
		rep ++;
	}
	return (1);
}
